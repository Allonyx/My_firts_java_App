     void llenar_tablas()
     {
   
     Menu  me = new Menu();
     DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
     DefaultTableModel model2 = (DefaultTableModel)jTable2.getModel();
     Object[] capturar_datos=new Object[10];
     
     int i = 0;
     
     for(i=0;i<me.vec_Contribuyentes.length;i++)
     {
       if (me.vec_Contribuyentes[i].getTipo_contribuyente() == 1)
    
           
       {
           capturar_datos[0]=me.vec_Contribuyentes[i].getTipo_contribuyente();
           capturar_datos[1]=me.vec_Contribuyentes[i].getCedula();
           capturar_datos[2]=me.vec_Contribuyentes[i].getNombre();
           capturar_datos[3]=me.vec_Contribuyentes[i].getPagos_credit_anuales();
           capturar_datos[4]=me.vec_Contribuyentes[i].getValor_patrimonio();
           model.addRow(capturar_datos);
       }
       else 
       {
           capturar_datos[0]=me.vec_Contribuyentes[i].getTipo_contribuyente();
           capturar_datos[1]=me.vec_Contribuyentes[i].getNit();
           capturar_datos[2]=me.vec_Contribuyentes[i].getNombre();
           capturar_datos[3]=me.vec_Contribuyentes[i].getValor_ingresos_anuales();
           capturar_datos[4]=me.vec_Contribuyentes[i].getValor_desc_tributarios();
           capturar_datos[5]=me.vec_Contribuyentes[i].getValor_patrimonio();
           model2.addRow(capturar_datos);
       }
    }
     
     }
     
     
     
